#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *list;

	if (lst && f)
	{
		list = lst;
		while (list)
		{
			f(list);
			list = list->next;
		}
	}
}
