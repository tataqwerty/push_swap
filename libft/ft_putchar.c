#include "libft.h"

void	ft_putchar(int c)
{
	int				bytes;
	unsigned char	s[4];

	bytes = ft_bytes(c);
	if (bytes == 1)
		s[0] = (unsigned char)c;
	else if (bytes == 2)
	{
		s[0] = ((c >> 6) & 31) | 192;
		s[1] = (c & 63) | 128;
	}
	else if (bytes == 3)
	{
		s[0] = ((c >> 12) & 15) | 224;
		s[1] = ((c >> 6) & 63) | 128;
		s[2] = (c & 63) | 128;
	}
	else if (bytes == 4)
	{
		s[0] = ((c >> 18) & 7) | 240;
		s[1] = ((c >> 12) & 63) | 128;
		s[2] = ((c >> 6) & 63) | 128;
		s[3] = (c & 63) | 128;
	}
	write(1, s, bytes);
}
